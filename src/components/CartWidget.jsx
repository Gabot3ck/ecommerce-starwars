import { useContext } from "react";
import { Link } from "react-router-dom";
import {MiContext} from "../contexts/CartContext";

function CartWidget() {

    const {enviarCant,cart} = useContext(MiContext);

    return (<> 
            <Link className="link-cart" to={"/Cart"}>
                <i className="bi bi-cart2 me-2"></i>
                {cart.length>0 && enviarCant()}
            </Link>
        </>)
}

export default CartWidget;





































































































































